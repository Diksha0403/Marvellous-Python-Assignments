def add(val1,val2):
    Addition = val1+val2
    return Addition
    

def sub(val1,val2):
    Subtraction = val1-val2
    return Subtraction
    

def mul(val1,val2):
    Multiplication = val1*val2
    return Multiplication

def div(val1,val2):
    Division = val1/val2
    return Division
 